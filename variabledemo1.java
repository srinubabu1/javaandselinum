
public class variabledemo1 {

	public static void main(String[] args) {
		System.out.println("15");
		System.out.println("15");
		System.out.println("15+5");
		System.out.println("15+20");
		int a=15;
		System.out.println("a");
		System.out.println("a");
		System.out.println("a+5");
		System.out.println("a+20");

	}

}
